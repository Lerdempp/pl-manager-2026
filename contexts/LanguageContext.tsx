export const LanguageProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<Language>(() => {
    // Load from localStorage or default to 'en'
    try {
      const saved = localStorage.getItem('gameLanguage') as Language;
      return saved && (saved === 'en' || saved === 'tr') ? saved : 'en';
    } catch (e) {
      console.warn('[Storage] Failed to access localStorage for gameLanguage:', e);
      return 'en';
    }
  });
  const [languageSelected, setLanguageSelected] = useState(() => {
    // Check if language was previously selected
    try {
      return localStorage.getItem('gameLanguage') !== null;
    } catch (e) {
      console.warn('[Storage] Failed to access localStorage for gameLanguage check:', e);
      return false;
    }
  });

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    try {
      localStorage.setItem('gameLanguage', lang);
      setLanguageSelected(true);
    } catch (e) {
      console.warn('[Storage] Failed to save gameLanguage to localStorage:', e);
      // Still set the state even if storage fails
      setLanguageSelected(true);
    }
  };

  const t = (key: string, params?: Record<string, string>): string => {
    let translation = translations[language][key] || key;
    // Replace placeholders like {teamName} with actual values
    if (params) {
      Object.keys(params).forEach(param => {
        translation = translation.replace(`{${param}}`, params[param]);
      });
    }
    return translation;
  };

  // Helper function to translate positions
  const translatePosition = (position: string): string => {
    const positionKey = `position.${position}`;
    return translations[language][positionKey] || position;
  };

  // Helper function to format date with translated month
  const formatDate = (date: Date): string => {
    const monthKey = `month.${date.toLocaleDateString('en-US', { month: 'short' }).toLowerCase()}`;
    const month = translations[language][monthKey] || date.toLocaleDateString('en-US', { month: 'short' });
    const day = date.getDate();
    const year = date.getFullYear();
    return `${month} ${day}, ${year}`;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, translatePosition, formatDate, languageSelected, setLanguageSelected }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};

